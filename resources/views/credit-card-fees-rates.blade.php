                                                                  <h2 style="color: #3baabd; padding-top: 20px;">Here's a brief explanation of the process:</h2>

                                                                  <p class="text-left" style="padding-top: 10px;">The card issuing banks decide how much they want to charge when you accept their credit cards. They consider factors such as processing method (swiped, keyed, e-commerce), card type (rewards, business, consumer), your business type (merchant category code), and a host of other variables to create a long, exhausting list of interchange fees.</p>

                                                                  <p class="text-left" style="padding-top: 10px;">Interchange fees are assessed net of refunds and chargebacks and mostly are made out of two parts consisting of a percentage and a flat transaction fee. For example, 1.51% plus $0.10 is the current Visa interchange fee for a swiped consumer credit card.</p>



 <p class="text-left" style="padding-top: 10px;">It's important to note that even though interchange rates don't vary among processors, it is possible to optimize interchange charges to achieve lower costs. Check out our information on interchange fees for more details on interchange optimization.</p>

                                                                  <p class="text-left" style="padding-top: 10px;">These institutions also make money by charging assessments on every transaction involving one of their credit cards. Like interchange, assessments are exactly the same for all credit card processors and no processor can give you a lower rate or a better deal on assessments.</p>

                                                                  <p class="text-left" style="padding-top: 10px;">However, assessments may be charged differently if you have a bundled pricing model. This flexibility is only possible because the processor has more control to manipulate pricing on a bundled pricing system.</p>

                                                                  <p class="text-left" style="padding-top: 10px;">The assessments for each card brand are listed below along with the details about when they apply. Assessments are changed periodically by the card brands, and this list is updated as changes are announced.</p>

                                                                  <p class="text-left" style="padding-top: 10px;">Caveat emptor: The only true assessment fee from each card brand is the percentage charge applied to volume. The various other fees such as network access, foreign handling, and so forth are charges incurred through processing behavior at the individual transaction level. The article refers to these fees collectively as assessments because these charges are consistent for all businesses. This simplification is done to help readers understand the high-level fee breakdown. </p>



