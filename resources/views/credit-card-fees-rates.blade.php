<h1 style="color: #3baabd; padding-top: 20px;">Volume</h1>

 <p class="text-left" style="padding-top: 10px;">With interchange plus pricing, the volume fee will be a single number such as 0.25%. With tiered pricing, the volume fees will be in the form of a qualified, mid-qualified and nonqualified rate, and there may be more than one set of tiers. <br><br>

Volume-based fees are levied on your business's sales volume. The competitiveness, consistency, and transparency of the volume-based markup are dependent on the pricing model that your merchant account utilizes.


