							<p class="text-left">These applications may work with hardware readers that can transfer smart card chip information to the application. However, most of the credit card readers that are available for smartphones are only able to read the older magnetic stripes. Some smartphones offer built-in NFC technology and can be used as card terminals for contactless payment cards without requiring additional external hardware.</p> 

