						  	<p class="text-left">SlashMyFees platform has many mentions to a credit card terminal, so we wanted to explain that term in more detail. A credit card terminal, also known as a point of sale terminal, payment terminal, EFTPOS terminal is a device which interfaces with payment cards to facilitate electronic funds transfers.</p> 

							<p class="text-left">There are various types of terminals available to merchants, although most have the same basic purpose and functions. These devices allow a merchant to insert, swipe, or manually enter the required credit/debit card information, to transmit this data to the merchant service provider for authorization and finally, to transfer funds to the merchant.</p>


							<p class="text-left">A few general facts:</p>

							<ul class="text-left">
								<li>Most newer model terminals not only process credit and debit cards but can also handle gift cards, checks, and such.</li> 
								<li>The majority of card terminals transmit data over a standard telephone line or an Internet connection (either wired or wireless).</li> 
								<li>Some also have the ability to cache transactional data and transmit the data to the gateway processor when a connection becomes available when immediate authorization is not available at the time the card was processed</li>
								<li>Some remote wireless terminals can even transmit card data using either cellular or satellite networks.</li>
							</ul>

							<p class="text-left">So the remaining question is, does every merchant who needs to process cards need a terminal and the answer is no.</p>

