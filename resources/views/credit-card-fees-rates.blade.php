							          <h1 class="text-left" style="color: #3baabd; text-decoration: underline;">Credit Card Processing Fees & Rates</h1>
                                                                  <p class="text-left" style="padding-top: 10px;">Credit card processing fees can be pretty straightforward once the components are analyzed individually. With this article, your helpful team at SlashMyFees will explain all of the different components of credit card processing costs to give you a solid understanding of the charges and how you can do some cost control</p>

                                                                  <p class="text-left" style="padding-top: 10px;">Let's start by clearing a misconception; not all credit card processing fees are negotiable.  The rate that you pay to process a credit card transaction is a combination of base costs and markups called merchant discount. Think of merchant discount as the retail price of credit card processing, base costs as raw material expenses and the markup as production costs.</p>

                                                                  <p class="text-left" style="padding-top: 10px;">Base costs usually account for the largest portion of expense (about 75% - 80%) followed by the markup (about 20% - 25%). In cases like this, there's a lot of room for savings. See the Cost Distribution section below to learn more about where your money goes.</p>



                                                                  <h1 class="text-left" style="color: #3baabd; padding-top: 20px; text-decoration: underline;">Base Credit Card Processing Fees</h1>
                                                                  <p class="text-left" style="padding-top: 10px;">Base credit card processing fees are made up of interchange fees and assessments, and they're the same for all processors. No processor can give you a lower rate or a better deal on base costs. For example, First Data - the largest credit card processor pays the same interchange fees and assessments as a small local bank.</p>

                                                                  <h2 style="color: #3baabd; padding-top: 20px;">Interchange Fees</h2>

                                                                  <p class="text-left" style="padding-top: 10px;">Interchange fees account for the most significant portion of credit card processing expense, and they are paid to card-issuing banks. The processors and the card brands such as Amex, Visa, MasterCard, and Discover don't get any revenue from the interchange fees.</p>

