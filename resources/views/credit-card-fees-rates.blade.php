<h1 style="color: #3baabd; padding-top: 20px;">Cost Distribution</h1>

<p class="text-left" style="padding-top: 10px;">With competitive pricing the majority of credit card processing costs are paid to your customers' issuing banks through interchange. The remaining costs are split among a varying number of layers such as the acquiring bank, processor, ISO(s), and equipment or software provider. Exactly how many layers there are depends on the vendor and your business's processing needs.<br><br>

Here's an example that illustrates how credit card processing costs are distributed. Let's pretend that you're processing a $50 transaction by swiping a customer's (consumer, non-reward) Visa credit card through your credit card machine. For this example, we'll assume that you used SlashMyFees to obtain a competitive interchange plus merchant account with rates of 20 basis points and $0.10 per transaction.<br><br>

Interchange:<br><br>

1.54% plus $0.10 = $0.87 goes to the issuing bank<br><br>

Assessments:<br><br>

0.11% plus $0.0195 to Visa when the transaction is authorized and another $0.003 when it's settled = $0.07 goes to Visa<br><br>

Card Markup:<br><br>

.20% plus $0.10 to the processor = $0.20 goes to the processor<br><br>

You are left with:<br><br>

$50 - $0.87 - $0.07 - $0.20 = $48.86 (2.28% overall effective rate)<br><br>

