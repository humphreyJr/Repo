<h1 style="color: #3baabd; padding-top: 20px;">Tiered or Bundled</h1>

 <p class="text-left" style="padding-top: 10px;">Tiered pricing, also referred to as bundled or bucket pricing, is named for the way a processor categorizes interchange fees into three pricing groups called qualified, mid-qualified and non-qualified. Although these three are the most common, this pricing model can have separate sets of tiers for various types of cards. For example, six-tier pricing where credit and debit cards each have their three tiers is gaining in popularity.<br><br>

On a bundled pricing model the processor uses something called an interchange qualification matrix to route interchange fees to the qualified, mid-qualified, or nonqualified tiers.<br><br>

A significant problem with tiered pricing is that interchange fees are often not disclosed on your merchant processing statement, and the processor may not disclose into which tier individual interchange fees are being routed. This reporting leaves you with no way to calculate exactly how much you're paying above the actual processing costs of interchange and assessments. This opacity in tiered pricing has led to the processing industry's shady reputation.
</p>



<h1 style="color: #3baabd; padding-top: 20px;">Inconsistent Buckets</h1>

 <p class="text-left" style="padding-top: 10px;">


Inconsistent buckets are the processing industry's term for, "there's no way to compare credit card processing quotes that are based on tiered pricing."<br><br>

Tiered pricing allows a processor to manipulate charges behind the scenes. Mainly, they can raise your cost without having to raise your rates. They do this by routing more interchange fees to the mid and non-qualified pricing tiers. Since there's no consistency regarding interchange qualification, it's impossible to compare tiered pricing among different processors.<br><br>

Let's look at an example to illustrate inconsistent buckets. Let's pretend that we have the following quotes from two different processors:<br><br>

Processor A:<br>
Qualified Rate: 1.49%<br>
Mid-Qualified Rate: 2.59%<br>
Non-Qualified Rate: 2.99%<br><br>

Processor B:<br>
Qualified Rate: 1.69%<br>
Mid-Qualified Rate: 2.25%<br>
Non-Qualified Rate: 2.49%<br><br>

Look only at the qualified rate; Processor A is offering a much better deal. What you don't know is how many interchange categories are being routed to the qualified tier. Processor A may be routing the majority of transactions to the mid and non-qualified tiers making Processor B the better option. Of course, there's no way to tell just by looking at the numbers.
</p>

