						  <p class="text-left" style="padding-top: 10px;">The network of banks and processors that carry billions of transactions between merchants, processors and banks are technological marvels. In just a matter of seconds, information about your pending transaction is passed by your terminal/software to a processor, and then through the card network to the issuing bank for approval. The issuing bank then sends an authorization or denial response back through the network to your processor before it finally ends up back at your terminal or software and finalizes the process.</p>

						  <p class="text-left" style="padding-top: 10px;">However, this complex process of obtaining authorization for a transaction is just the first step. Authorizations for the transaction must first be settled before the funds from the sales can be deposited into your business's bank account. Credit card transaction happens in a two-stage process consisting of authorization and settlement. Understanding this two-step process is necessary because different fees are incurred at each stage, and a failure in either step can result in increased costs and funds from the credit card sales not being deposited as expected.</p>

