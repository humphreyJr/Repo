<h1 style="color: #3baabd; padding-top: 20px;">Markups</h1>

 <p class="text-left" style="padding-top: 10px;">


The markup over interchange and assessments is the only area where you have the ability to negotiate credit card processing costs. The markup isn't all profit. It's split among all of the organizations that facilitate the processing of your business's transactions such as the acquiring bank, processor, ISO(s), gateway or software provider and others. The markup must cover the costs as well as profits for all of these entities.</p>
 <p class="text-left" style="padding-top: 10px;">
Markups differ significantly from one processor to the next both by amount, pricing model and the types of fees charged. These differences are why it's difficult to accurately compare credit card processing on the open market. Here at SlashMyFees, we only work with processors that are transparent and fair, offering competitive pricing that can be accurately compared by our users.</p>




<h1 style="color: #3baabd; padding-top: 20px;">Pricing Model</h1>

 <p class="text-left" style="padding-top: 10px;">
Interchange and assessments are the same for all processors. The method the processor uses to pass these costs to you is what is important. The two most common types of pricing are interchange plus and bundled also referred to as pass through and tiered, respectively. Each pricing model is outlined below, and there's also a detailed post comparing interchange plus vs. bundled pricing here.

