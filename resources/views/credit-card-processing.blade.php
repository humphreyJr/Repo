						  <h2 style="color: #3baabd; padding-top: 20px;">Cardholder</h2>

						  <p class="text-left" style="padding-top: 10px;">If you have a credit or debit card as most of us do, you are already pretty familiar with the role of the cardholder. A cardholder is someone who obtains a bankcard (credit or debit) from a card issuing bank with the promise of timely payment of debt incurred by its use. Cardholders then present that card to merchants as payment for goods or services.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Business Owner</h2>
						  <p class="text-left" style="padding-top: 10px;">The business owner is an individual engaged in the sale of goods or services. So, any company that maintains a merchant account that enables them to accept credit or debit cards as payment from customers for goods or services provided will fall into this category.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Acquiring or Merchant Bank</h2>
						  <p class="text-left" style="padding-top: 10px;">These banks are registered members of the card associations and often referred to as merchant banks because they contract with merchants to create and maintain accounts that allow the business to accept credit and debit cards, (i.e. merchant accounts). Acquiring banks provide merchants with equipment and software to allow the use of bank cards, promotional materials, customer service and other necessary aspects involved in card acceptance. The acquiring bank also deposits funds from credit card sales into a merchant's account.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Issuing or Cardholder Bank</h2>
						  <p class="text-left" style="padding-top: 10px;">As the name suggests, an issuing bank issues credit cards to consumers. The issuing banks are also members of the card associations. These banks pay acquiring banks for purchases that their cardholders make. If the transaction is a debit card transaction, then it is settled immediately with the account associated with the card. However, if the transaction is a credit card transaction then it is the cardholder's responsibility to pay their issuing bank under the terms of their credit card agreement.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Card Associations (American Express, Europay, MasterCard, and Visa)</h2>
						  <p class="text-left" style="padding-top: 10px;">These organizations are not banks, and they don't issue credit cards directly (except for American Express), or merchant accounts. Instead, they act as a custodian and clearing house for their respective card brands. They also function as the governing body of a community of financial institutions, Independent Sales Organizations (ISOs), and Membership Service Providers (MSPs) that work together in association to support credit card processing and electronic payments. </p> 

						  <p class="text-left" style="padding-top: 10px;">The primary responsibilities of the Card Association are to govern the members of their associations, including interchange fees and qualification guidelines, act as the arbiter between issuing and acquiring banks, maintain and improve the card network and their brand.</p> 

						  <p class="text-left" style="padding-top: 10px;">These associations also provide the infrastructure that relays the highly sensitive data between parties: Visa with its VisaNet network and MasterCard with its Banknet network.</p> 

						  <h1 class="text-left" style="color: #3baabd; padding-top: 20px;">Step 1: Credit Card Authorization Process</h1>

						  <h2 style="color: #3baabd; padding-top: 20px;">Cardholder</h2>
						  <p class="text-left" style="padding-top: 10px;">A cardholder begins a credit card transaction by presenting his or her card to a business owner as payment for goods or services.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Business Owner</h2>
						  <p class="text-left" style="padding-top: 10px;">The business owner uses its credit card machine, software or gateway to transmit the cardholder's information and the details of the transaction to their acquiring bank, or the bank's processor.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Acquiring or Merchant Bank</h2>
						  <p class="text-left" style="padding-top: 10px;">The acquiring bank (or its processor) captures the transaction information and routes it through the appropriate card network to the cardholder's issuing bank for approval.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Card Association Network</h2>
						  <p class="text-left" style="padding-top: 10px;">The card transaction information is routed between issuing and acquiring banks through the association's network.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Issuing or Cardholder Bank</h2>
						  <p class="text-left" style="padding-top: 10px;">The credit card issuer receives the transaction information from the acquiring bank (or its processor) through the secure network and responds by approving or declining the transaction after checking to ensure, among other things, that the transaction information is valid, the cardholder has sufficient balance to make the purchase and that the account is in good standing.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Card Association Network</h2>
						  <p class="text-left" style="padding-top: 10px;">The card issuer sends a response code back through the appropriate network to the acquiring bank (or its processor).</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Business Owner</h2>
						  <p class="text-left" style="padding-top: 10px;">The response code reaches the merchant's terminal, software or gateway and is stored in a batch file awaiting settlement.</p> 


						  <h1 class="text-left" style="color: #3baabd; padding-top: 20px;">Step 2: Credit Card Clearing and Settlement Process</h1>

						  <h2 style="color: #3baabd; padding-top: 20px;">Business Owner</h2>
						  <p class="text-left" style="padding-top: 10px;">The business owner begins the settlement process by sending their batch of approved authorizations to their acquiring bank (or the bank's processor). Authorization batches are typically sent at the close of each business day.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Acquiring or Merchant Bank</h2>
						  <p class="text-left" style="padding-top: 10px;">The acquiring bank (or its processor) reconciles and transmits the batch of authorizations through interchange via the appropriate card association's network. It deposits funds from sales into the merchant's account via the automated clearinghouse (ACH) and debits its merchant's account for processing fees as outlined by the merchant's processing agreement.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Card Association Network</h2>
						  <p class="text-left" style="padding-top: 10px;">The card association debits the issuing bank's account and credits the acquiring bank's account for the net amount of the authorizations which is gross receipts less interchange and network fees.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Issuing or Cardholder Bank</h2>
						  <p class="text-left" style="padding-top: 10px;">The card issuing bank pays the acquiring bank for its cardholder's purchases.</p> 

						  <h2 style="color: #3baabd; padding-top: 20px;">Cardholder</h2>
						  <p class="text-left" style="padding-top: 10px;">The cardholder is responsible for repaying his or her issuing bank for the purchase and any accrued interest and fees associate with the card agreement.</p> 
