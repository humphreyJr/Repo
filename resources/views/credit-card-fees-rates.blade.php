<h1 style="color: #3baabd; padding-top: 20px;">Interchange Plus or Pass Through</h1>

 <p class="text-left" style="padding-top: 10px;">With interchange plus pricing, the processor's markup isn't dependent on interchange qualification. This separation of costs keeps the processor's markup the same regardless of the type of card you accept, or how your process it. These are not segmented as qualified, mid-qualified or non-qualified rates as they would be with the bundled model.<br><br>

The processor earns a fixed percentage regardless of the underlying interchange. For example, 0.25% is a case of an interchange plus rate quote. No fancy tiers, no qualification at the processor level -- just one single rate that gets added to actual cost (interchange).<br><br>

Interchange plus allows for interchange credits on refunded transactions. For example, when you issue a customer a refund, you are supposed to receive a partial credit of the interchange fee paid on the original transaction. This refund credit is not granted in bundled pricing models, but processors are capable of issuing interchange refunds on interchange plus pricing. <br><br>

Another benefit of interchange plus is that it allows businesses to reap the benefits of decreases in interchange fees. For example, companies with interchange plus pricing will benefit from lower debit card charges from the Durbin Amendment of October 1, 2011.<br><br>

Like with bundled pricing, processors are capable of manipulating costs under an interchange-plus pricing model, too. For example, interchange plus pricing does not guarantee that a processor will pass assessments at the real cost, issue interchange credits, or refrain from applying a discount to refund volume. This article goes into more detail about the dangers of becoming pricing model-complacent.<br><br>

